package org.puzzler.model;

/**
 * Enumeration of piece colors / identifiers used to distinguish piece types.
 *
 * <p>The enum values correspond to the different piece shapes used in levels.
 * Note: the enum preserves the project spelling (e.g. {@code LIGTH_RED}) to avoid
 * changing existing references. If you prefer the corrected spelling, update
 * all usages across the project.</p>
 */
public enum Color {
    YELLOW,
    LIGTH_RED,
    PINK,
    LIGHT_BLUE,
    DARK_MINT,
    PURPLE,
    ORANGE,
    DARK_BLUE,
    GREEN,
    DARK_RED,
    LIGHT_MINT,
    BLUE
}
